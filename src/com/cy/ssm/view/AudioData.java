package com.cy.ssm.view;

public class AudioData {
	private int code;
	private AudioView data;
	private String desc;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public AudioView getData() {
		return data;
	}
	public void setData(AudioView data) {
		this.data = data;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
}
